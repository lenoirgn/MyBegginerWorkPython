def fonction1_sem1(a:float, b:float) -> float:
    """ Renvoie a - b.

    Précondition : /
    Exemple(s) :
    $$$ fonction1_sem1(42.0, 3.0)
    approx(39.0)
    $$$ fonction1_sem1(0.3, 0.2)
    approx(0.1)
    """
    return a - b

